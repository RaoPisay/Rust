use std::{i8, i16, i32, i64, f32, f64, isize, usize, u8, u16, u32, u64, char};

fn main() {
    
    println!("5 + 4 = {}", 5 + 4);
    println!("5 - 4 = {}", 5 - 4);
    println!("5 / 4 = {}", 5 / 4);
    println!("5 % 4 = {}", 5 % 4);
    println!("5 * 4 = {}", 5 * 4);

    println!("abs(-4) = {}", (-4i8).abs());
    println!("4 ^ 6 = {}", 4i32.pow(6));
    println!("sqrt root of 9 = {}", 9f32.sqrt());
    println!("cube root of 9 = {}", 9f32.cbrt());
    println!("Round of 1.23 = {}", 1.23f64.round());
    println!("Round of 1.26 = {}", 1.29f64.round());
    println!("Floor of 1.23 = {}", 1.23f64.floor());
    println!("Ceil of 1.23 = {}", 1.23f64.ceil());
    println!("e ^ 2 = {}", 2f64.exp());
    println!("log(2) = {}", 2f32.ln());
    println!("log10(2) = {}", 2f32.log10());
    println!("90 to radians = {}", 90f64.to_radians());
    println!("90 to degrees = {}", 90f64.to_degrees());
    println!("Min of 4, 5 = {}", 4i8.min(5i8));
    println!("Max of 4, 5 = {}", 4i8.max(5i8));
    println!("Sin(90) = {}", 90f64.sin());
    println!("Sin(1) = {}", 1f64.sin());
    
 





}
